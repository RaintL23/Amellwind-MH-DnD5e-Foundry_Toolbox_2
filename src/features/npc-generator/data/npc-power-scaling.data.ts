import { RARITY_ORDER, type RarityTier } from "@/shared/types";

/** Template tier 0–3 (shown to users as Power Tier 1–4). */
export interface NpcPowerBand {
  hitDiceMin: number;
  hitDiceMax: number;
  /** Challenge rating as a decimal (0.125 = 1/8). */
  cr: number;
  /** MM / MPMM stat block used as a baseline for this band. */
  mmReference: string;
  weaponRarityIndex: number;
  /** Flat AC bonus for upgraded armor quality at this band. */
  acBonus: number;
  /** +N to primary ability and Con on the stat block (meaningful from CR 4+). */
  statBoost: number;
  /** +N to all proficient skills — subtle bump between CR 2 and 3. */
  skillBoost: number;
  /** Flat bonus to weapon attack rolls (added to PB + ability mod). */
  attackHitBonus: number;
  /** Extra damage dice on the primary weapon (e.g. 1d10 → 2d10). */
  bonusDamageDice: number;
  /** Flat damage added to weapon attacks after ability modifier. */
  flatDamageBonus: number;
}

